/** LLM 프롬프트용 재료 요약 */
export function materialsCatalogSummary(): string {
  return MATERIALS.map((m) => {
    const tags = m.tags.length ? ` [${m.tags.join(',')}]` : '';
    return `- ${m.id} (${m.nameKo}): ${m.description} | 항복 ${m.yieldStrengthMPa}MPa, 최대 ${m.maxTempC}°C, 비용 티어 ${m.costTier}/5${tags}`;
  }).join('\n');
}
