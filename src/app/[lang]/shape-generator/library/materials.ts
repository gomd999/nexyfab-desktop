/**
 * materials.ts — 재료 카탈로그 (L4)
 *
 * 제조 현장에서 실제로 쓰이는 주요 재료의 물성, 비용, 적합 기준을 정규화.
 * L5 Composition Agent 가 IntakeSpec (환경·하중·특수요구) 와 매칭한다.
 */

export type MaterialCategory =
  | 'plastic'
  | 'metal-soft'    // 알루미늄, 구리, 아연
  | 'metal-hard'    // 강철, 스테인리스, 티타늄
  | 'composite'
  | 'ceramic';

export interface Material {
  id: string;
  nameKo: string;
  nameEn: string;
  category: MaterialCategory;
  description: string;

  // 기계적 물성
  densityKgM3: number;             // kg/m³
  tensileStrengthMPa: number;      // 인장강도 MPa
  yieldStrengthMPa: number;        // 항복강도 MPa
  elongationPct: number;           // 연신율 %
  hardnessHB: number;              // 브리넬 경도 (근사)

  // 열적 물성
  maxTempC: number;                // 연속 사용 최대 온도 °C
  minTempC: number;                // 최소 온도 °C
  thermalConductivity: number;     // W/m·K

  // 환경 내성 (0~100)
  corrosionResistance: number;     // 부식 내성
  uvResistance: number;            // 자외선
  chemicalResistance: number;      // 화학 약품
  waterResistance: number;         // 방수/흡수

  // 전기
  electricalConductivity: 'insulator' | 'conductor' | 'semi';

  // 특성 태그 (IntakeSpec.specialReqs 와 매칭)
  tags: string[];  // transparent | conductive | food_safe | biocompatible ...

  // 제조법 호환
  compatibleMethods: string[];  // method ID 리스트

  // 경제
  pricePerKgUsd: number;            // 평균 달러/kg
  costTier: 1 | 2 | 3 | 4 | 5;      // 종합 비용 티어

  // 예산 적합 (0~100)
  sustainabilityScore: number;      // 재활용성/친환경

  bestFor: string[];
  limitations: string[];
}

